package com.soaint.educacion.jbossfuse7.Types;

public class Persona {
	
	private String nombre,apellido, nuevoApellido;

	public String getNombre() {
		return nombre;
	}

	public void setNombre(String nombre) {
		this.nombre = nombre;
	}

	public String getApellido() {
		return apellido;
	}

	public void setApellido(String apellido) {
		this.apellido = apellido;
	}

	public String getNuevoApellido() {
		return nuevoApellido;
	}

	public void setNuevoApellido(String nuevoApellido) {
		this.nuevoApellido = nuevoApellido;
	}
	
	
}
